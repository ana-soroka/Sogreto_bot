    # –ü–æ–∫–∞–∑–∞—Ç—å —Å–ø–∏—Å–æ–∫ —Å—É—â–µ—Å—Ç–≤—É—é—â–∏—Ö –±—ç–∫–∞–ø–æ–≤
    echo ""
    echo "üìÅ –°–ø–∏—Å–æ–∫ –±—ç–∫–∞–ø–æ–≤:"
    ls -lh "$BACKUP_DIR"/sogreto_bot_*.db

