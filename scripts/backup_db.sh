    echo "‚ùå –û—à–∏–±–∫–∞: –§–∞–π–ª –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö $DB_FILE –Ω–µ –Ω–∞–π–¥–µ–Ω"
    exit 1
