    echo "‚úÖ –ë—ç–∫–∞–ø —Å–æ–∑–¥–∞–Ω: $BACKUP_FILE"

    # –ü–æ–ª—É—á–∏—Ç—å —Ä–∞–∑–º–µ—Ä —Ñ–∞–π–ª–∞
    SIZE=$(du -h "$BACKUP_FILE" | cut -f1)
    echo "üìä –†–∞–∑–º–µ—Ä –±—ç–∫–∞–ø–∞: $SIZE"

    # –£–¥–∞–ª–∏—Ç—å —Å—Ç–∞—Ä—ã–µ –±—ç–∫–∞–ø—ã (–æ—Å—Ç–∞–≤–∏—Ç—å —Ç–æ–ª—å–∫–æ –ø–æ—Å–ª–µ–¥–Ω–∏–µ MAX_BACKUPS)
    BACKUP_COUNT=$(ls -1 "$BACKUP_DIR"/sogreto_bot_*.db 2>/dev/null | wc -l)

    if [ "$BACKUP_COUNT" -gt "$MAX_BACKUPS" ]; then
        echo "üßπ –£–¥–∞–ª–µ–Ω–∏–µ —Å—Ç–∞—Ä—ã—Ö –±—ç–∫–∞–ø–æ–≤ (–æ—Å—Ç–∞–≤–ª—è–µ–º –ø–æ—Å–ª–µ–¥–Ω–∏–µ $MAX_BACKUPS)..."
        ls -1t "$BACKUP_DIR"/sogreto_bot_*.db | tail -n +$((MAX_BACKUPS + 1)) | xargs rm -f
        echo "‚úÖ –°—Ç–∞—Ä—ã–µ –±—ç–∫–∞–ø—ã —É–¥–∞–ª–µ–Ω—ã"
